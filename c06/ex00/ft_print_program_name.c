#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (0 < argc)
	{
		while (0 != argv[0][i])
		{
			write(1, &argv[0][i], 1);
		i++;
		}
	}
	write(1, "\n", 1);
}
